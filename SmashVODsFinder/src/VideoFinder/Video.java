package VideoFinder;

public class Video {
	private String title;
	private String url;
	
	
	public String getTitle() {
		return this.title;
	}
	
	public void setTitle(String newTitle) {
		this.title = newTitle;
	}
	
	public String getURL() {
		return this.url;
	}
	
	public void setURL(String newUrl) {
		this.url = newUrl;
	}

}
